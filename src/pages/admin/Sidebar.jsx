export default function Sidebar() {
  const location = useLocation();
  const { logout } = useAuth();
  const navigate = useNavigate();
  const [logoError, setLogoError] = useState(false);

  const handleLogout = () => {
    logout();
    navigate('/login');
  };

  return (
    <aside className="w-64 bg-white text-gray-700 min-h-screen flex flex-col fixed left-0 top-0 overflow-y-auto z-50 border-r border-gray-200 shadow-xl">
      <div className="px-4 py-4 border-b border-gray-100 flex flex-col items-center bg-white">
        {/* Logo */}
        <div className="w-full flex flex-col items-center justify-center">
           {!logoError ? (
             /* User Logo - Optimized for White Background */
             <div className="w-full max-w-[180px] flex items-center justify-center py-1 transition-transform hover:scale-105">
                 <img 
                   src="/logo.png" 
                   alt="Kirti Construction" 
                   className="w-full h-auto object-contain"
                   onError={() => setLogoError(true)}
                 />
             </div>
           ) : (
             /* Fallback Logo */
             <div className="transition-transform hover:scale-105 w-full max-w-[180px] flex items-center justify-center">
                <img 
                  src="/admin-logo.svg" 
                  alt="Kirti Construction" 
                  className="w-40 h-auto object-contain"
                />
             </div>
           )}
        </div>
      </div>
      
      <nav className="flex-1 py-3 px-3 space-y-0.5 bg-white">
        {navItems.map((item) => {
          const isActive = location.pathname === item.path || (item.path !== '/admin' && location.pathname.startsWith(item.path));
          
          return (
            <Link
              key={item.name}
              to={item.path}
              className={`flex items-center gap-3 px-4 py-2 rounded-lg transition-all duration-200 text-sm font-semibold group ${
                isActive 
                  ? 'bg-amber-50 text-amber-700 shadow-sm ring-1 ring-amber-200' 
                  : 'text-gray-500 hover:bg-gray-50 hover:text-gray-900'
              }`}
            >
              <span className={`transition-colors duration-200 ${isActive ? 'text-amber-600' : 'text-gray-400 group-hover:text-gray-600'}`}>
                {item.icon}
              </span>
              {item.name}
            </Link>
          );
        })}
      </nav>
      
      <div className="p-3 border-t border-gray-100 space-y-2 bg-gray-50">
        <Link 
          to="/" 
          className="flex items-center justify-center w-full px-4 py-2 bg-white text-gray-700 rounded-lg hover:bg-amber-500 hover:text-white transition-all text-sm font-semibold border border-gray-200 hover:border-amber-500 shadow-sm"
        >
          View Live Site
        </Link>
        <button
          onClick={handleLogout}
          className="flex items-center justify-center w-full px-4 py-2 bg-gray-800 text-white rounded-lg hover:bg-red-600 transition-colors text-sm font-medium shadow-md"
        >
          Sign Out
        </button>
      </div>
    </aside>
  );
}
